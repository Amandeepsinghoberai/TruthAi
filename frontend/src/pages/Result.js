import React from 'react';
import { useLocation, useNavigate, Link } from 'react-router-dom';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Progress } from '@/components/ui/progress';
import { CheckCircle2, XCircle, ArrowLeft, Upload, AlertTriangle } from 'lucide-react';

const Result = () => {
  const location = useLocation();
  const navigate = useNavigate();
  const { result, file, scan } = location.state || {};

  // Handle both fresh result and historical scan
  const data = result || scan;

  if (!data) {
    return (
      <div className="min-h-screen bg-gray-50 py-12">
        <div className="container mx-auto px-4 max-w-3xl text-center">
          <AlertTriangle className="w-16 h-16 mx-auto text-yellow-500 mb-4" />
          <h2 className="text-2xl font-bold mb-4">No Result Data</h2>
          <p className="text-gray-600 mb-6">Please upload a file first to see results</p>
          <Link to="/upload">
            <Button className="bg-[#1a3c5e] hover:bg-[#2d7dd2]">
              Go to Upload
            </Button>
          </Link>
        </div>
      </div>
    );
  }

  const verdict = data.verdict;
  const confidence = data.confidence;
  const fileName = file || data.fileName || 'Unknown';
  const fileType = data.fileType;
  const isReal = verdict === 'REAL';
  const frameBreakdown = data.frameBreakdown || data.details?.frame_breakdown || [];

  return (
    <div className="min-h-screen bg-gray-50 py-12">
      <div className="container mx-auto px-4 max-w-4xl">
        {/* Back Button */}
        <Button
          variant="ghost"
          onClick={() => navigate('/dashboard')}
          className="mb-6"
        >
          <ArrowLeft className="w-4 h-4 mr-2" />
          Back to Dashboard
        </Button>

        {/* Main Result Card */}
        <Card className="mb-8">
          <CardHeader className="text-center">
            <div className="flex justify-center mb-4">
              {isReal ? (
                <CheckCircle2 className="w-24 h-24 text-[#28a745]" />
              ) : (
                <XCircle className="w-24 h-24 text-[#dc3545]" />
              )}
            </div>
            <CardTitle className="text-4xl mb-2">
              <Badge 
                className={`text-2xl px-6 py-3 ${
                  isReal ? 'bg-[#28a745]' : 'bg-[#dc3545]'
                }`}
              >
                {verdict}
              </Badge>
            </CardTitle>
            <p className="text-gray-600 text-lg">
              {confidence}% Confidence
            </p>
          </CardHeader>
          <CardContent>
            <div className="space-y-6">
              {/* Confidence Meter */}
              <div>
                <div className="flex justify-between text-sm mb-2">
                  <span className="font-semibold">Confidence Score</span>
                  <span>{confidence}%</span>
                </div>
                <Progress 
                  value={confidence} 
                  className="h-4"
                  indicatorClassName={isReal ? 'bg-[#28a745]' : 'bg-[#dc3545]'}
                />
              </div>

              {/* File Info */}
              <div className="bg-gray-50 p-4 rounded-lg">
                <h3 className="font-semibold mb-2">File Information</h3>
                <div className="space-y-1 text-sm">
                  <p><span className="text-gray-600">File Name:</span> <span className="font-medium">{fileName}</span></p>
                  <p><span className="text-gray-600">Type:</span> <span className="font-medium capitalize">{fileType}</span></p>
                  {data.framesAnalyzed && (
                    <p><span className="text-gray-600">Frames Analyzed:</span> <span className="font-medium">{data.framesAnalyzed}</span></p>
                  )}
                  {data.timestamp && (
                    <p><span className="text-gray-600">Scanned:</span> <span className="font-medium">{new Date(data.timestamp).toLocaleString()}</span></p>
                  )}
                </div>
              </div>

              {/* Interpretation */}
              <div className="bg-blue-50 border border-blue-200 p-4 rounded-lg">
                <h3 className="font-semibold mb-2 flex items-center">
                  <AlertTriangle className="w-5 h-5 mr-2 text-blue-600" />
                  What does this mean?
                </h3>
                <p className="text-sm text-gray-700">
                  {isReal ? (
                    <>
                      Our AI models have determined with <strong>{confidence}% confidence</strong> that this content appears to be <strong>authentic</strong> and has not been manipulated or generated by AI.
                    </>
                  ) : (
                    <>
                      Our AI models have detected with <strong>{confidence}% confidence</strong> that this content shows signs of <strong>manipulation or AI generation</strong>. This could be a deepfake, face-swap, or AI-generated content.
                    </>
                  )}
                </p>
                {data.details?.detection_method && (
                  <p className="text-xs text-gray-600 mt-2">
                    Detection Method: {data.details.detection_method}
                  </p>
                )}
              </div>

              {/* Model Details Breakdown */}
              {data.details?.model_details && (
                <div className="bg-gray-50 border border-gray-200 p-4 rounded-lg">
                  <h3 className="font-semibold mb-3">Detailed Analysis (Dual-Model System)</h3>
                  <div className="space-y-3 text-sm">
                    {/* Deepfake Detection Model */}
                    <div className="bg-white p-3 rounded">
                      <div className="flex items-center justify-between mb-2">
                        <span className="font-medium">Model 1: Deepfake Detection</span>
                        <Badge 
                          className={
                            data.details.model_details.deepfake_detection.verdict === 'REAL' 
                              ? 'bg-[#28a745]' 
                              : 'bg-[#dc3545]'
                          }
                        >
                          {data.details.model_details.deepfake_detection.verdict}
                        </Badge>
                      </div>
                      <p className="text-xs text-gray-600">
                        Specializes in detecting face-swaps and facial manipulation
                      </p>
                      <div className="mt-2 text-xs">
                        <span>Real: {(data.details.model_details.deepfake_detection.real_prob * 100).toFixed(1)}%</span>
                        <span className="ml-4">Fake: {(data.details.model_details.deepfake_detection.fake_prob * 100).toFixed(1)}%</span>
                      </div>
                    </div>

                    {/* AI-Generated Detection Model */}
                    <div className="bg-white p-3 rounded">
                      <div className="flex items-center justify-between mb-2">
                        <span className="font-medium">Model 2: AI-Generated Content Detection</span>
                        <Badge 
                          className={
                            data.details.model_details.ai_generated_detection.verdict === 'REAL' 
                              ? 'bg-[#28a745]' 
                              : 'bg-[#dc3545]'
                          }
                        >
                          {data.details.model_details.ai_generated_detection.verdict}
                        </Badge>
                      </div>
                      <p className="text-xs text-gray-600">
                        Specializes in detecting AI-generated images (Gemini, DALL-E, Midjourney, Stable Diffusion)
                      </p>
                      <div className="mt-2 text-xs">
                        <span>Real: {(data.details.model_details.ai_generated_detection.real_prob * 100).toFixed(1)}%</span>
                        <span className="ml-4">AI-Generated: {(data.details.model_details.ai_generated_detection.ai_generated_prob * 100).toFixed(1)}%</span>
                      </div>
                    </div>

                    <div className="text-xs text-gray-500 italic mt-2">
                      Final verdict is based on ensemble prediction combining both models
                    </div>
                  </div>
                </div>
              )}

              {/* Frame Breakdown for Videos */}
              {frameBreakdown && frameBreakdown.length > 0 && (
                <div>
                  <h3 className="font-semibold mb-4">Frame-by-Frame Analysis</h3>
                  <div className="space-y-2 max-h-64 overflow-y-auto">
                    {frameBreakdown.map((frame, idx) => (
                      <div key={idx} className="flex items-center justify-between p-3 bg-gray-50 rounded">
                        <span className="text-sm font-medium">Frame {frame.frame_number}</span>
                        <div className="flex items-center space-x-3">
                          <Badge 
                            variant={frame.verdict === 'REAL' ? 'success' : 'destructive'}
                            className={`text-xs ${frame.verdict === 'REAL' ? 'bg-[#28a745]' : 'bg-[#dc3545]'}`}
                          >
                            {frame.verdict}
                          </Badge>
                          <span className="text-sm text-gray-600">{frame.confidence}%</span>
                        </div>
                      </div>
                    ))}
                  </div>
                </div>
              )}
            </div>
          </CardContent>
        </Card>

        {/* Action Buttons */}
        <div className="flex flex-col sm:flex-row gap-4 justify-center">
          <Link to="/upload">
            <Button className="bg-[#1a3c5e] hover:bg-[#2d7dd2] w-full sm:w-auto">
              <Upload className="w-4 h-4 mr-2" />
              Analyze Another File
            </Button>
          </Link>
          <Link to="/survey">
            <Button variant="outline" className="w-full sm:w-auto">
              Share Feedback
            </Button>
          </Link>
        </div>
      </div>
    </div>
  );
};

export default Result;
